class Solution {
    public void rotate(int[] nums, int k) {
        int n=nums.length;
        // int []arr=new int[n];
        // for(int i=0;i<n;i++){
        //     int p=(i+k)%n;
        //     arr[p]=nums[i];
        // }
        // for(int i=0;i<n;i++){
        //     nums[i]=arr[i];
        // }

        k=k%n;
        reverse(nums,0,n-1);
        reverse(nums,0,k-1);
        reverse(nums,k,n-1);
    }
    public static void reverse(int[]nums,int l,int r){
        while(l<r){
            int temp=nums[l];
            nums[l]=nums[r];
            nums[r]=temp;
            l++;
            r--;
        }
    }
}